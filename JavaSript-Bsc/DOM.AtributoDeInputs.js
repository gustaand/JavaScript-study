/* --- Atributos de inputs ---

- className: nos muestra el nombre de la clase.

- value: nos muestra el valor del input.

- type: se usa para poder modificar directamente.

- accept: sirve para los inputs file y se usa para decir que tipo de arquivo se acepta en el file.

- form: sirve para los formulários, para poder crear un botón fuera del formulário y asignarlo al formulário por ID. ej: form = "ID"

- minLength: sirve para decir cuál es el mínimo de caracteres necesários para poder enviar el input.

- placeholder: Sirve para dejar un mensaje en el input que al clicar se borará automaticamente.

- required: nos permite decir si es requerido o no, (con true).

- style: sirve para cambiar el estilo del elemento (camelCase).

*/